public class Charactor {
    public static void main(String[] args) {
        // ■ 문자 타입 : 2byte
        // '문자' 를 넣기위한 타입 (문자열이 아님)
        // 문자는 '' 로 감싸서 리터럴을 만든다.
        char ch1 = 'A';
        char ch2 = '박';
        System.out.println(ch1);
        System.out.println(ch2);

        // ■ 문자열 
        // String 클래스를 사용한다. (non-primitive type)
        String str = "ezen";
        System.out.println(str);
    }
}
