public class Printer {
    void printer(int i)
    {
        System.out.println(i);
    }
    void printer(boolean i)
    {
        System.out.println(i);
    }
    void printer(double i)
    {
        System.out.println(i);
    }
    void printer(String i)
    {
        System.out.println(i);
    }
}   
